void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	print(char string[])
{
	int	i;

	i = 0;
	while (string[i] != '\0' && i < 2147483647)
	{
		write(1, &string[i], 1);
		i++;
	}
}

void	print_x_num(int x)
{
	int	num_array[10];
	int	i;
	int	j;

	i = 0;
	while (i < x)
	{
		num_array[i] = i;
		i++;
	}
	i = 0;
	while (i < 10)
	{
		j = 0;
		while (j < x)
		{
			ft_putchar(num_array[i] + '0');
			num_array[i] += 1;
			j++;
		}
		i++;
	}
}

void	ft_print_combn(int n)
{
	int	i;
	int	j;

	i = 0;
	while (i <= n * n)
	{
		j = 0;
		while (j < n)
		{
			print_x_num(n);
			j++;
		}
		i++;
	}
}

